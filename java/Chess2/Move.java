class Move
{
  // instance fields

  private int FromX;
  private int FromY;
  private int ToX;
  private int ToY;

  // constructors

  public Move (int pFromX, 
               int pFromY,
               int pToX,
               int pToY)
  {
    this.FromX = pFromX;
    this.FromY = pFromY;
    this.ToX = pToX;
    this.ToY = pToY;
  }

  // methods


}